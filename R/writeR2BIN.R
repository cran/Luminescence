  ##in TAG information on the SEL are storred, here the values are copied to TAG
  ##before export
  object@METADATA[,"TAG"] <- ifelse(object@METADATA[,"SEL"] == TRUE, 1, 0)
  
  
# SET FILE AND VALUES -----------------------------------------------------

con<-file(file, "wb")

    ##get records
    n.records <- max(object@METADATA[,"ID"])
    
    ##output
