           endian="little")
  
  ##SYSTEMID
  writeBin(as.integer(object@METADATA[ID,"SYSTEMID"]),
           con,
           size = 2,
           endian="little")
  
  ##Further distinction need to fully support format version 03 and 04 separately
  if(version == 03){
  
